def score_technical(close: pd.Series) -> dict:
    rsi = compute_rsi(close)
    macd_line, signal_line, hist = compute_macd(close)
    sma, std, z, upper, lower = compute_mean_reversion(close)
    sma50 = close.rolling(50).mean()
    sma200 = close.rolling(200).mean()

    last_rsi = rsi.iloc[-1]
    last_macd, last_signal = macd_line.iloc[-1], signal_line.iloc[-1]
    last_z = z.iloc[-1] if not np.isnan(z.iloc[-1]) else 0
    last_price = close.iloc[-1]
    last_sma50 = sma50.iloc[-1] if not np.isnan(sma50.iloc[-1]) else last_price
    last_sma200 = sma200.iloc[-1] if len(sma200.dropna()) > 0 else last_price

    score = 0
    reasons = []

    if last_rsi < 30:
        score += 1; reasons.append(f"RSI is {last_rsi:.1f} (oversold, bullish)")
    elif last_rsi > 70:
        score -= 1; reasons.append(f"RSI is {last_rsi:.1f} (overbought, bearish)")
    else:
        reasons.append(f"RSI is {last_rsi:.1f} (neutral)")

    if last_macd > last_signal:
        score += 1; reasons.append("MACD line above signal line (bullish momentum)")
    else:
        score -= 1; reasons.append("MACD line below signal line (bearish momentum)")

    if last_z <= -2:
        score += 1; reasons.append(f"Price is {last_z:.2f} std below its 20-day mean (oversold, reversion-up expected)")
    elif last_z >= 2:
        score -= 1; reasons.append(f"Price is {last_z:.2f} std above its 20-day mean (overbought, reversion-down expected)")
    else:
        reasons.append(f"Price is within normal range of 20-day mean (z={last_z:.2f})")

    if last_price > last_sma50 > last_sma200:
        score += 1; reasons.append("Price above 50 & 200-day MA (uptrend)")
    elif last_price < last_sma50 < last_sma200:
        score -= 1; reasons.append("Price below 50 & 200-day MA (downtrend)")
    else:
        reasons.append("Mixed trend signal from moving averages")

    return {
        "rsi": last_rsi, "macd": last_macd, "signal": last_signal, "hist": hist.iloc[-1],
        "z_score": last_z, "upper_band": upper.iloc[-1], "lower_band": lower.iloc[-1],
        "score": score, "reasons": reasons,
        "overbought_oversold": (
            "Overbought" if (last_rsi > 70 or last_z >= 2) else
            "Oversold" if (last_rsi < 30 or last_z <= -2) else "Neutral"
        ),
    }


def score_fundamental(info: dict) -> dict:
    score = 0
    reasons = []

    pe = info.get("trailingPE")
    roe = info.get("returnOnEquity")
    margins = info.get("profitMargins")
    rev_growth = info.get("revenueGrowth")
    earn_growth = info.get("earningsGrowth")
    de = info.get("debtToEquity")

    if pe is not None:
        if 0 < pe < 25:
            score += 1; reasons.append(f"Trailing P/E of {pe:.1f} is reasonable")
        elif pe >= 40:
            score -= 1; reasons.append(f"Trailing P/E of {pe:.1f} looks expensive")

    if roe is not None:
        if roe > 0.15:
            score += 1; reasons.append(f"Strong ROE of {roe*100:.1f}%")
        elif roe < 0:
            score -= 1; reasons.append("Negative ROE")

    if margins is not None:
        if margins > 0.10:
            score += 1; reasons.append(f"Healthy profit margin of {margins*100:.1f}%")
        elif margins < 0:
            score -= 1; reasons.append("Negative profit margin")

    if rev_growth is not None:
        if rev_growth > 0.05:
            score += 1; reasons.append(f"Revenue growing at {rev_growth*100:.1f}%")
        elif rev_growth < 0:
            score -= 1; reasons.append("Revenue is declining")

    if earn_growth is not None and earn_growth < -0.10:
        score -= 1; reasons.append("Earnings declining sharply")

    if de is not None and de > 200:
        score -= 1; reasons.append(f"High debt-to-equity of {de:.0f}%")

    if not reasons:
        reasons.append("Insufficient fundamental data available from Yahoo Finance")

    return {"score": score, "reasons": reasons}


def final_recommendation(tech_score: int, fund_score: int):
    total = tech_score + fund_score
    if total >= 3:
        return "STRONG BUY", total
    elif total >= 1:
        return "BUY", total
    elif total <= -3:
        return "STRONG SELL", total
    elif total <= -1:
        return "SELL", total
    else:
        return "HOLD", total


