def compute_rsi(close: pd.Series, period: int = 14) -> pd.Series:
    delta = close.diff()
    gain = delta.clip(lower=0)
    loss = -delta.clip(upper=0)
    avg_gain = gain.ewm(alpha=1 / period, min_periods=period, adjust=False).mean()
    avg_loss = loss.ewm(alpha=1 / period, min_periods=period, adjust=False).mean()
    rs = avg_gain / avg_loss.replace(0, np.nan)
    rsi = 100 - (100 / (1 + rs))
    return rsi.fillna(50)


def compute_macd(close: pd.Series, fast: int = 12, slow: int = 26, signal: int = 9):
    ema_fast = close.ewm(span=fast, adjust=False).mean()
    ema_slow = close.ewm(span=slow, adjust=False).mean()
    macd_line = ema_fast - ema_slow
    signal_line = macd_line.ewm(span=signal, adjust=False).mean()
    hist = macd_line - signal_line
    return macd_line, signal_line, hist


def compute_mean_reversion(close: pd.Series, window: int = 20):
    sma = close.rolling(window).mean()
    std = close.rolling(window).std()
    z = (close - sma) / std.replace(0, np.nan)
    upper_band = sma + 2 * std
    lower_band = sma - 2 * std
    return sma, std, z, upper_band, lower_band


def compute_pivot_points(df: pd.DataFrame, method: str = "Daily") -> dict:
    """Classic pivot points using the last COMPLETED period's H/L/C."""
    if method == "Weekly":
        agg = df.resample("W").agg({"High": "max", "Low": "min", "Close": "last"}).dropna()
    elif method == "Monthly":
        agg = df.resample("ME").agg({"High": "max", "Low": "min", "Close": "last"}).dropna()
    else:
        agg = df[["High", "Low", "Close"]].dropna()

    if len(agg) < 2:
        row = agg.iloc[-1]
    else:
        row = agg.iloc[-2]  # last completed period

    H, L, C = row["High"], row["Low"], row["Close"]
    P = (H + L + C) / 3
    R1 = 2 * P - L
    S1 = 2 * P - H
    R2 = P + (H - L)
    S2 = P - (H - L)
    R3 = H + 2 * (P - L)
    S3 = L - 2 * (H - P)
    return {"R3": R3, "R2": R2, "R1": R1, "Pivot": P, "S1": S1, "S2": S2, "S3": S3,
            "Ref High": H, "Ref Low": L, "Ref Close": C}


