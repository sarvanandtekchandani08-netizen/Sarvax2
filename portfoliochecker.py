def build_portfolio_returns(tickers: list, period: str = "2y") -> pd.DataFrame:
    series_list = []
    for t in tickers:
        s = fetch_close_series(t, period)
        if not s.empty:
            series_list.append(s)
    if not series_list:
        return pd.DataFrame()
    prices = pd.concat(series_list, axis=1, join="inner")
    returns = prices.pct_change().dropna()
    return returns


def historical_var(port_returns: pd.Series, portfolio_value: float, pct: float = 1):
    downside = -np.percentile(port_returns, pct) * portfolio_value
    upside = np.percentile(port_returns, 100 - pct) * portfolio_value
    return downside, upside


def parametric_var(port_returns: pd.Series, portfolio_value: float, z: float = Z_99):
    mu, sigma = port_returns.mean(), port_returns.std()
    downside = -(mu - z * sigma) * portfolio_value
    upside = (mu + z * sigma) * portfolio_value
    return downside, upside


def stressed_var(port_returns: pd.Series, portfolio_value: float, window: int = 60, pct: float = 1):
    if len(port_returns) < window + 5:
        window = max(10, len(port_returns) // 2)
    rolling_vol = port_returns.rolling(window).std()
    if rolling_vol.dropna().empty:
        return None
    stress_end = rolling_vol.idxmax()
    stress_window = port_returns.loc[:stress_end].tail(window)
    downside = -np.percentile(stress_window, pct) * portfolio_value
    upside = np.percentile(stress_window, 100 - pct) * portfolio_value
    start_date = stress_window.index.min()
    end_date = stress_window.index.max()
    return downside, upside, start_date, end_date


